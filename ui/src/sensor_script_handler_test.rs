fn router(state: AppState) -> Router {
    Router::new()
        .route("/sensors/generate", get(get_generate_select))
        .route("/sensors/generate/form", get(get_generate_form))
        .route("/sensors/generate/script", post(post_generate_script))
        .with_state(state)
}

